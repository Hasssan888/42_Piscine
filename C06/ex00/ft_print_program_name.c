#include <unistd.h>

int	main(int arc, char **arv)
{
	int	i;

	(void)arc;
	i = 0;
	while (arv[0][i])
	{
		write(1, &arv[0][i], 1);
		i++;
	}
	write(1, "\n", 1);
}
