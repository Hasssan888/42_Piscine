#include <unistd.h>

int	main(int arc, char **arv)
{
	int	i;
	int	j;

	(void)arc;
	i = 1;
	while (arv[i])
	{
		j = 0;
		while (arv[i][j])
		{
			write(1, &arv[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}
