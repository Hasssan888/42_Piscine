int	ft_sqrt(int nb)
{
	int		i;
	float	r;

	if (nb < 1)
		return (0);
	r = 1;
	i = 0;
	while (i < 100)
	{
		r = (r * r + nb) / (2.0 * r);
		i++;
	}
	i = r;
	if (i * i == nb)
		return (i);
	if ((i - 1) * (i - 1) == nb)
		return (i - 1);
	if ((i + 1) * (i + 1) == nb)
		return (i + 1);
	return (0);
}
